# Operadores Aritmeticos
print(3 + 4)
print(3 - 4)
print(3 * 4)
print(3 / 4)
print(10 % 3)
print(10 // 3)
print(2 ** 3)

print('Hola' + ' Python ' + 'Que tal')
print('Hola ' + str(5))
print('Hola ' * 5)

# Operadores Comparativos
print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

print('Hola' > 'python')
print('Hola' < 'python')
print('aaaa' >= 'abaa') # Ordenacion alfabetica por ASCII
print(len('aaa') >= len('abaa')) # Cuenta caracteres
print('hola' <= 'python')
print('hola' == 'python')
print('hola' != 'python')

# Operadores Logicos
print(3 > 4 and 'hola' > 'python')
print(3 > 4 or 'hola' > 'python')
print(3 < 4 and 'hola' < 'python')
print(3 < 4 or 'hola' < 'python')
print(not(3 > 4))