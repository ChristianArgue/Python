# Type hints

my_string_variable = 'My String Variable'
print(my_string_variable)
print(type(my_string_variable))

my_string_variable = 5
print(my_string_variable)
print(type(my_string_variable))

my_typed_varibale: str = 'My Typed String variable'
print(my_typed_varibale)
print(type(my_typed_varibale))

my_typed_varibale: int = 5
print(my_typed_varibale)
print(type(my_typed_varibale))